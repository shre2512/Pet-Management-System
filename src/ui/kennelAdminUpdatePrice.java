    private void btnUpdatePriceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdatePriceActionPerformed
        try {
            // TODO add your handling code here:
            DefaultTableModel model = (DefaultTableModel) tblKennel.getModel();
            int selectedRowIndex = tblKennel.getSelectedRow();
            int kennelID = (int) model.getValueAt(selectedRowIndex, 0);
            int newPrice = Integer.parseInt(txtKennelPrice.getText());
            
            databaseConnection.executeKennelPriceUpdate("UPDATE kennel SET price_per_day = ? WHERE id = ?", newPrice, kennelID);
   
            JOptionPane.showMessageDialog(this, "Price Updated Successfully!");
            txtKennelName.setText("");
            txtKennelPrice.setText("");
            populateTable();
        } catch (Exception ex) {
            Logger.getLogger(kennelAdminUpdatePrice.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnUpdatePriceActionPerformed

