    private void populateTable()
    {
        try {
            ResultSet result = databaseConnection.executeSelect("SELECT * FROM kennel");
            DefaultTableModel model = (DefaultTableModel) tblKennel.getModel();
            model.setRowCount(0);
            while(result.next())
            {
                Object[] row = new Object[4];
                row[0] = result.getInt("id");
                row[1] = result.getString("kennel_name");
                row[2] = result.getString("kennel_address");
                row[3] = result.getInt("price_per_day");
                model.addRow(row);
            }
        } catch (Exception ex) {
            Logger.getLogger(kennelAdminUpdatePrice.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
